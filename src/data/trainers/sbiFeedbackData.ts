export function scoreSbiField(userText: string, keywords: string[]): number {
  if (!userText.trim()) return 0;
  const lower = userText.toLowerCase();
  const matched = keywords.filter(kw => lower.includes(kw.toLowerCase()));
  return Math.min(100, Math.round((matched.length / Math.max(keywords.length, 1)) * 100));
}

export function scoreSbiTotal(
  userS: string, userB: string, userI: string,
  scenario: SbiScenario
): { situation: number; behavior: number; impact: number; total: number } {
  const situation = scoreSbiField(userS, scenario.keywords.situation);
  const behavior = scoreSbiField(userB, scenario.keywords.behavior);
  const impact = scoreSbiField(userI, scenario.keywords.impact);
  const total = Math.round((situation + behavior + impact) / 3);
  return { situation, behavior, impact, total };
}
